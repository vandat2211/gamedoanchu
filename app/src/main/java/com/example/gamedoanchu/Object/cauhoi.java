package com.example.gamedoanchu.Object;

public class cauhoi {
    public String ten,dapan,anh;

    public cauhoi() {
    }



    public cauhoi( String ten, String dapan, String anh) {

        this.ten = ten;
        this.dapan = dapan;
        this.anh = anh;
    }


    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getDapan() {
        return dapan;
    }

    public void setDapan(String dapan) {
        this.dapan = dapan;
    }

    public String getAnh() {
        return anh;
    }

    public void setAnh(String anh) {
        this.anh = anh;
    }


}
